    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            int seleccionado = Tabla.getSelectedRow();

            int id = Integer.parseInt(Tabla.getValueAt(seleccionado, 1).toString());
            String codigo = Tabla.getValueAt(seleccionado, 2).toString();
            String nombre = Tabla.getValueAt(seleccionado, 3).toString();
            String desc = Tabla.getValueAt(seleccionado, 4).toString();
            String carac = Tabla.getValueAt(seleccionado, 5).toString();
            int stock = Integer.parseInt(Tabla.getValueAt(seleccionado, 6).toString());
            double precio = Double.parseDouble(Tabla.getValueAt(seleccionado, 7).toString());

            Producto prod = new Producto(codigo, nombre, desc, carac, stock, precio);
            prod.setIdProducto(id);

            frmProductoCrear crear = new frmProductoCrear();
            crear.editar(prod);
            //crear.setVisible(true);

        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Primero seleccione un producto.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed


