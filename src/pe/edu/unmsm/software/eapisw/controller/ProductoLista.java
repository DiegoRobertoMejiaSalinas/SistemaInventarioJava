    private void btnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarActionPerformed
        ProductoServiceImpl productoService = new ProductoServiceImpl();
        DefaultTableModel modelo = null;

        if (radioID.isSelected()) {
            modelo = productoService.ordenarArbolRNID();
        } else if (radioCodigo.isSelected()) {
            modelo = productoService.ordenarArbolRNCodigo();
        } else if (radioNombre.isSelected()) {
            modelo = productoService.ordenarArbolRNNombre();
        }
        Tabla.setModel(modelo);
        darFormaTabla();
    }//GEN-LAST:event_btnOrdenarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        ProductoServiceImpl productoService = new ProductoServiceImpl();

        String texto = txtBusqueda.getText();
        DefaultTableModel modelo = null;

        if (texto.equalsIgnoreCase("")) {
            modelo = productoService.mostrarArbolRN();
        } else {
            modelo = null;
            if (radioID.isSelected()) {
                modelo = productoService.busquedaSecuencial(texto, "id");
            } else if (radioCodigo.isSelected()) {
                modelo = productoService.busquedaSecuencial(texto, "codigo");
            } else if (radioNombre.isSelected()) {
                modelo = productoService.busquedaSecuencial(texto, "nombre");
            }

        }
        Tabla.setModel(modelo);
        darFormaTabla();
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int confirmacion = JOptionPane.showConfirmDialog(null, "Â¿Estas seguro de eliminar este producto?", "Confirmar", 2);

        if (confirmacion == 0) {
            try {
                int seleccionado = Tabla.getSelectedRow();

                String codigo = Tabla.getValueAt(seleccionado, 2).toString();

                ProductoDAOImpl dao = new ProductoDAOImpl();
                dao.delete(codigo);

            } catch (ArrayIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(null, "Primero seleccione un producto.");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

