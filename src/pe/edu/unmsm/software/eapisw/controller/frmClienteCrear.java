    public void editar(Cliente cliente) {
        txtAMaterno.setText(cliente.getaMaterno());
        txtAPaterno.setText(cliente.getaPaterno());
        txtDireccion.setText(cliente.getDireccion());
        txtEmail.setText(cliente.getEmail());
        txtNombre.setText(cliente.getNombre());
        txtNumDoc.setText(cliente.getNumeroDocumento());
        txtTelefono.setText(cliente.getTelefono());
        txtCodigoCliente.setText(cliente.getCodigoCliente());

        if (cliente.getTipoDocumento().equalsIgnoreCase("dni")) {
            comboTipo.setSelectedIndex(0);
        } else if (cliente.getTipoDocumento().equalsIgnoreCase("carnetDeExtranjeria")) {
            comboTipo.setSelectedIndex(1);
        } else {
            comboTipo.setSelectedIndex(2);
        }
        crear = false;
        setVisible(true);
    }

