    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        nombre = txtNombre.getText();
        aPaterno = txtAPaterno.getText();
        aMaterno = txtAMaterno.getText();
        numDoc = txtNumDoc.getText();
        direccion = txtDireccion.getText();
        telefono = txtTelefono.getText();
        email = txtEmail.getText();
        codigoCliente = txtCodigoCliente.getText();

        if (nombre.length() == 0 || aPaterno.length() == 0 || aMaterno.length() == 0
                || tipoDoc.length() == 0 || numDoc.length() == 0 || codigoCliente.length() == 0) {
            JOptionPane.showMessageDialog(null, "No se puede registrar al cliente con campos obligatorios vacios.");
            return;
        } else {
            if (crear) {
                ClienteDAOImpl creado = new ClienteDAOImpl();

                Cliente cliente = new Cliente();
                cliente.setNombre(nombre);
                cliente.setCodigoCliente(codigoCliente);
                cliente.setDireccion(direccion);
                cliente.setEmail(email);
                cliente.setNumeroDocumento(numDoc);
                cliente.setTelefono(telefono);
                cliente.setTipoDocumento(tipoDoc);
                cliente.setaMaterno(aMaterno);
                cliente.setaPaterno(aPaterno);

                boolean trueDato = creado.create(cliente);
                if (trueDato) {
                    this.dispose();
                }
            } else {
                ClienteDAOImpl creado = new ClienteDAOImpl();

                Cliente cliente = new Cliente();
                cliente.setNombre(nombre);
                cliente.setCodigoCliente(codigoCliente);
                cliente.setDireccion(direccion);
                cliente.setEmail(email);
                cliente.setNumeroDocumento(numDoc);
                cliente.setTelefono(telefono);
                cliente.setTipoDocumento(tipoDoc);
                cliente.setaMaterno(aMaterno);
                cliente.setaPaterno(aPaterno);

                boolean trueDato = creado.update(cliente);
                if (trueDato) {
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void comboTipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboTipoItemStateChanged
        String tipo = (String) comboTipo.getSelectedItem();
        if (tipo.equalsIgnoreCase("dni")) {
            tipoDoc = "dni";
        } else if (tipo.equalsIgnoreCase("carnet de extranjeria")) {
            tipoDoc = "carnetDeExtranjeria";
        } else {
            tipoDoc = "pasaporte";
        }
    }//GEN-LAST:event_comboTipoItemStateChanged

