    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        nombre = txtNombre.getText();
        aPaterno = txtAPaterno.getText();
        aMaterno = txtAMaterno.getText();
        numDoc = txtNumDoc.getText();
        direccion = txtDireccion.getText();
        telefono = txtTelefono.getText();
        email = txtEmail.getText();
        login = txtLogin.getText();
        password = txtPass.getText();

        if (nombre.length() == 0 || aPaterno.length() == 0 || aMaterno.length() == 0
                || tipoDoc.length() == 0 || numDoc.length() == 0 || login.length() == 0
                || password.length() == 0) {
            JOptionPane.showMessageDialog(null, "No se puede registrar al trabajador con campos obligatorios vacios.");
            return;

        } else {

            try {
                sueldo = Double.parseDouble(txtSueldo.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "No se pudo convertir a digitos el sueldo ingresado.");
                return;
            }

            if (sueldo > 0) {
                if (crear) {
                    TrabajadorDAOImpl creado = new TrabajadorDAOImpl();

                    Trabajador trab = new Trabajador();
                    trab.setNombre(nombre);
                    trab.setDireccion(direccion);
                    trab.setEmail(email);
                    trab.setNumeroDocumento(numDoc);
                    trab.setTelefono(telefono);
                    trab.setTipoDocumento(tipoDoc);
                    trab.setaMaterno(aMaterno);
                    trab.setaPaterno(aPaterno);
                    trab.setAcceso(rol);
                    trab.setLogin(login);
                    trab.setPassword(password);
                    trab.setSueldo(sueldo);

                    boolean trueDato = creado.create(trab);

                    if (trueDato) {
                        this.dispose();
                    }
                } else {
                    TrabajadorDAOImpl creado = new TrabajadorDAOImpl();

                    Trabajador trab = new Trabajador();
                    trab.setNombre(nombre);
                    trab.setDireccion(direccion);
                    trab.setEmail(email);
                    trab.setNumeroDocumento(numDoc);
                    trab.setTelefono(telefono);
                    trab.setTipoDocumento(tipoDoc);
                    trab.setaMaterno(aMaterno);
                    trab.setaPaterno(aPaterno);
                    trab.setAcceso(rol);
                    trab.setLogin(login);
                    trab.setPassword(password);
                    trab.setSueldo(sueldo);

                    boolean trueDato = creado.update(trab);

                    if (trueDato) {
                        this.dispose();
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "El sueldo debe ser mayor a 0.");
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void comboTipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboTipoItemStateChanged
        String tipo = (String) comboTipo.getSelectedItem();
        if (tipo.equalsIgnoreCase("dni")) {
            tipoDoc = "dni";
        } else if (tipo.equalsIgnoreCase("carnet de extranjeria")) {
            tipoDoc = "carnetDeExtranjeria";
        } else {
            tipoDoc = "pasaporte";
        }
    }//GEN-LAST:event_comboTipoItemStateChanged

    private void comboRolItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboRolItemStateChanged
        String role = (String) comboRol.getSelectedItem();
        if (role.equalsIgnoreCase("administrador")) {
            rol = "administrador";
        } else {
            rol = "trabajador";
        }
    }//GEN-LAST:event_comboRolItemStateChanged

