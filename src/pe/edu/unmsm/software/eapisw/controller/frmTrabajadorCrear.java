    public void editar(Trabajador trab) {
        txtAMaterno.setText(trab.getaMaterno());
        txtAPaterno.setText(trab.getaPaterno());
        txtDireccion.setText(trab.getDireccion());
        txtEmail.setText(trab.getEmail());
        txtLogin.setText(trab.getLogin());
        txtNombre.setText(trab.getNombre());
        txtNumDoc.setText(trab.getNumeroDocumento());
        txtPass.setText(trab.getPassword());
        txtSueldo.setText(String.valueOf(trab.getSueldo()));
        txtTelefono.setText(trab.getTelefono());

        if (trab.getAcceso().equalsIgnoreCase("administrador")) {
            comboRol.setSelectedIndex(0);
        } else {
            comboRol.setSelectedIndex(1);
        }

        if (trab.getTipoDocumento().equalsIgnoreCase("dni")) {
            comboTipo.setSelectedIndex(0);
        } else if (trab.getTipoDocumento().equalsIgnoreCase("carnetDeExtranjeria")) {
            comboTipo.setSelectedIndex(1);
        } else {
            comboTipo.setSelectedIndex(2);
        }
        crear = false;
        setVisible(true);
    }

