    public void mostrarTabla() {
        TrabajadorServiceImpl trabajadorService = new TrabajadorServiceImpl();
        DefaultTableModel modelo = trabajadorService.mostrarArbolAVL();
        Tabla.setModel(modelo);
        darFormaTabla();
    }

