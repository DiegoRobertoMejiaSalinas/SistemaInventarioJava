    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int seleccionado = Tabla.getSelectedRow();

            int id = Integer.parseInt(Tabla.getValueAt(seleccionado, 1).toString());
            String nombre = Tabla.getValueAt(seleccionado, 2).toString();
            String paterno = Tabla.getValueAt(seleccionado, 3).toString();
            String materno = Tabla.getValueAt(seleccionado, 4).toString();
            String tipoDoc = Tabla.getValueAt(seleccionado, 5).toString();
            String numDoc = Tabla.getValueAt(seleccionado, 6).toString();
            String direccion = Tabla.getValueAt(seleccionado, 7).toString();
            String telefono = Tabla.getValueAt(seleccionado, 8).toString();
            String email = Tabla.getValueAt(seleccionado, 9).toString();
            String login = Tabla.getValueAt(seleccionado, 10).toString();
            String acceso = Tabla.getValueAt(seleccionado, 11).toString();
            Double sueldo = Double.parseDouble(Tabla.getValueAt(seleccionado, 12).toString());

            Trabajador trabajador = new Trabajador();
            trabajador.setIdPersona(id);
            trabajador.setNombre(nombre);
            trabajador.setaPaterno(paterno);
            trabajador.setaMaterno(materno);
            trabajador.setTipoDocumento(tipoDoc);
            trabajador.setNumeroDocumento(numDoc);
            trabajador.setDireccion(direccion);
            trabajador.setTelefono(telefono);
            trabajador.setEmail(email);
            trabajador.setLogin(login);
            trabajador.setAcceso(acceso);
            trabajador.setSueldo(sueldo);

            frmTrabajadorCrear crear = new frmTrabajadorCrear();
            crear.editar(trabajador);
            //crear.setVisible(true);

        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(null, "Primero seleccione un trabajador.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

